    private void detailKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_detailKeyPressed
        int selectedRow = detail.getSelectedRow();
                if ((evt.getKeyCode() == KeyEvent.VK_DELETE || evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) && selectedRow != -1) {
                    int confirm = JOptionPane.showConfirmDialog(
                            this,
                            "Are you sure you want to delete this row?",
                            "Delete Confirmation",
                            JOptionPane.YES_NO_OPTION
                    );
                    if (confirm == JOptionPane.YES_OPTION) {
                       DefaultTableModel model = (DefaultTableModel) detail.getModel();
                       model.removeRow(selectedRow);
                       JOptionPane.showMessageDialog(this, "Delete Success", "Information", JOptionPane.INFORMATION_MESSAGE);
                       table.setEnabled(true);
                       cari.setEnabled(true);
                       reset();
                    }
                }
    }//GEN-LAST:event_detailKeyPressed

