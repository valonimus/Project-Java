    public void resetall(){
        DefaultTableModel m = (DefaultTableModel) detail.getModel();
        boolean tablekosong = m.getRowCount() == 0;
        boolean semuatfkosong = total.getText().isEmpty()&&
                                table.getText().isEmpty()&&
                                cari.getText().isEmpty()&&
                                bayar.getText().isEmpty()&&
                                desk.getText().isEmpty()&&
                                kembalian.getText().isEmpty();
        if (tablekosong && semuatfkosong){
            JOptionPane.showMessageDialog(this, "Nothing to reset.", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        int konfirmasi = JOptionPane.showConfirmDialog(
        this, "Are you sure want to reset order?", "Reset Confirmation",
                JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == JOptionPane.YES_OPTION){
            m.setRowCount(0);
            total.setText("");
            table.setText("");
            cari.setText("");
            bayar.setText("");
            desk.setText("");
            kembalian.setText("");
            table.setEnabled(true);
            
            JOptionPane.showMessageDialog(this, "Data has been successfully reset", "Reset", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }
    public void reset(){
         total.setText("");
         table.setText("");
         cari.setText("");
         desk.setText("");
    }
